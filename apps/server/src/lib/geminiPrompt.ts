import type { InferenceDetection, SymptomSet, TriageLevel } from '@pinequest/types'

// YOLO class_name (snake_case) → УI дээр харуулах нэр.
const CLASS_LABEL: Record<string, string> = {
  caries: 'Цоорсон шүд',
  cavity: 'Цоорлын хөндий',
  crack: 'Шүдний хугарал, гэмтэл',
}

const SYMPTOM_LABEL: Record<keyof SymptomSet, string> = {
  swelling: 'хаван, хавдар',
  painDisturbingSleepOrEating: 'шөнө өвддөг/юм идхэд өвддөг',
  fever: 'халуурах',
  gumPimpleOrFistula: 'Буйланд цорго үүссэн',
  trauma: 'гэмтэл, хугарал',
}

// Triage зэрэг → эмчид хандах яаралтай байдлын чиглүүлэг.
const URGENCY_BY_LEVEL: Record<TriageLevel, string> = {
  green:
    'яаралтай биш — одоогийн зурагт аюулын тод шинж ажиглагдсангүй, дараагийн ээлжит улирлын хяналтаар үзүүлэхэд хангалттай',
  yellow:
    'ойрын 1-2 долоо хоногт төлөвлөгөөтэйгээр шүдний эмчид үзүүлэх — яаралтай биш ч хойшлуулахгүй',
  red:
    'аль болох хурдан, өнөөдөр эсвэл маргааш шүдний эмчид хандах; өвдөлт, хаван, халуурал нэмэгдвэл яаралтай тусламжид хандах',
}

/** Gemini унавал / тохиргоогүй үед ашиглах энгийн зөвлөмж. */
export const fallbackAdvice = (level: TriageLevel, count: number): string => {
  if (level === 'red')
    return `Яаралтай тусламж шаардлагатай. ${count} шүдний гадаргууд  цоорлын хөндий танигдлаа. Хоног алдахгүйгээр эмчилгээ хийлгэх, эмчид үзүүлэх шаардлагатай.`
  if (level === 'yellow')
    return `${count} шүдний гадаргууд цоорол танигдлаа. Эмчлүүлэх шүд байгаа тул ойрын хугацаанд шүдний эмчид үзүүлээрэй.`
  return 'Амны хөндий харьцангуй эрүүл. Дараагийн улирлын хяналтанд орно уу.'
}

/** Нас, илрүүлэлт, triage, асуумжид тулгуурлан Gemini-д өгөх монгол prompt-ийг угсарна. */
export const buildAdvicePrompt = (params: {
  triageLevel: TriageLevel
  detections: InferenceDetection[]
  symptoms: SymptomSet
  age: string
}): string => {
  const findingLines = params.detections.length
    ? params.detections
        .slice()
        .sort((a, b) => b.confidence - a.confidence)
        .map(
          (d, i) =>
            `  ${i + 1}. ${CLASS_LABEL[d.className] ?? d.className} — магадлал ${(d.confidence * 100).toFixed(0)}%`,
        )
        .join('\n')
    : '  (Зургаас шүдний цоорол, өөрчлөлт танигдсангүй.)'

  const symptomLines = (Object.keys(params.symptoms) as Array<keyof SymptomSet>)
    .filter((k) => params.symptoms[k])
    .map((k) => `  • ${SYMPTOM_LABEL[k] ?? k}`)
    .join('\n')

  const age = params.age || 'тодорхойгүй'
  const urgency = URGENCY_BY_LEVEL[params.triageLevel]

  return `Та хүүхдийн шүдний нарийн мэргэжлийн эмч. Та сурагчийн асран хамгаалагчтай нүүр тулан, тайван, дулаахан, ойлгомжтой ярьж байна. Бодит эмч хүн шиг найрсаг ярь — эцэг эхийн санааг тайвшруул, гэхдээ шударга, тодорхой зөвлө. Хариуг ЗААВАЛ цэвэр монгол хэлээр бич. Хятад, орос, англи үг болон эмнэлзүйн хүнд нэр томьёо огт хэрэглэхгүй.

ЧУХАЛ: Зураг дээрх илрүүлэлт (detection) болон аюулын зэрэглэл (triage)-ийг АГ загвар (YOLO) аль хэдийн гаргасан — энэ нь АЛБАН ЁСНЫ үр дүн. Та үүнийг өөрчлөхгүй, зөвхөн асран хамгаалагчид зориулсан ойлгомжтой ЗӨВЛӨМЖ бичнэ. Энэ нь онош биш, эмчид цаг алдалгүй чиглүүлэх урьдчилсан шалгалт.

═══════════════════════════════
ХҮҮХДИЙН МЭДЭЭЛЭЛ
═══════════════════════════════
Нас                     : ${age}
Аюулын зэрэг (triage)   : ${params.triageLevel}  →  ${urgency}

ЗАГВАРЫН ИЛРҮҮЛСЭН ШҮДНҮҮД (албан ёсны, өөрчлөхгүй):
${findingLines}

АСУУМЖИЙН ШИНЖ ТЭМДЭГ:
${symptomLines || '  (Цоорсон шүдний гадаргуу танигдсангүй)'}

═══════════════════════════════
ХАРИУ БИЧИХ ЗААВАР
═══════════════════════════════
Доорх JSON талбар бүрийг ${age} насны хүүхдэд яг тохируулж бөглө. Талбар бүр 2-3
богино, жигд урсгалтай өгүүлбэр — хүн ярьж буй мэт чөлөөтэй, гэхдээ цэвэрхэн. Илрүүлсэн
шүднүүд, асуумжийн шинж тэмдэг, нас гурвыг хооронд нь холбож, утга төгөлдөр зөвлө.

  • advice     — Эмчийн амнаас гарах ГОЛ дүгнэлт. Мэндчилгээ бичихгүй, шууд хүүхдийн
                 одоогийн байдлыг хэл: илрүүлсэн шүд хэд байгаа, асуумжид ямар шинж
                 тэмдэг тэмдэглэгдсэн, эдгээр нь юу гэсэн үг болохыг 2-3 өгүүлбэрээр
                 энгийнээр тайлбарла. Аюулгүй найруулга ЗААВАЛ дагана:
                 – Ногоон зэрэг бол "Зурагт орсон шүднүүд харьцангуй эрүүл харагдаж байна"
                   гэж хэл; "цоорол огт байхгүй" гэж бат батлахгүй — нүдэнд үл харагдах
                   завсрын цоорол байж болзошгүйг зөөлөн сануул.
                 – Илрүүлэлт бага итгэлцэлтэй (50%-аас доош) бол "...байж магадгүй",
                   "...шиг харагдаж байна" гэж болгоомжтой найруул.
                 – Шар/улаан зэрэг эсвэл халуурах, хаван, шөнийн өвдөлт байвал хэр
                 яаралтай эмчид хандахыг тодорхой, гэхдээ айлгахгүйгээр хэл.
  • homeCare   — Яг одоо гэртээ хийх алхмууд: өвдөлт намдаах, юу ажиглах, юунаас зайлсхийх.
  • brushing   — ${age} насанд тохирсон шүд угаах заавар. Зөвхөн тухайн насанд тохирох
                 хувилбарыг сонгож хэл: фторын агууламж (3 хүртэл нас 500ppm, 3-аас дээш
                 1500ppm, савлагааны шошгоор шалгана), оохойн хэмжээ (1-3 нас будааны
                 ширхэг, 3-6 нас вандуй/чигчий хурууны хумсны хэмжээ), өдөрт өглөө сэрээд
                 болон орой унтахын өмнө 2 удаа, 7 хүртэл насны хүүхдийн оройн угаалтыг
                 том хүн хийж өгөх, шүдний завсрын утас хэрэглэх.
  • diet       — Шүд бэхжүүлэх хоол хүнс (кальцитай сүү/цагаан идээ, хатуу ааруул, ус) ба
                 хязгаарлах зүйл (чихэр, амтат ундаа, чихэртэй цай, наалддаг чихэр, шүд
                 угаасны дараа юм идэхгүй).
  • prevention — Цаашид цоорол БА шүдний эгнээ гажихаас урьдчилан сэргийлэх: сүүн шүдээ
                 эрт авхуулахгүй байх, удаан хуруу хөхөх/эрүүгээ тулах зэрэг зуршлыг
                 засах, хатуу хоол сайн зажилж эрүү нүүрний хөгжлийг дэмжих, улирал бүр
                 тогтмол хяналтад орох.
  • nextStep   — Дараагийн алхам: "${urgency}" гэдгийг хүнд ээлтэйгээр, ойролцоогоор
                 хэзээ эмчид очихыг хэл.

Зөвхөн дараах бүтэцтэй JSON-ийг буцаана. Markdown, тайлбар, өөр текст огт бичихгүй:
{
  "advice": "...",
  "homeCare": "...",
  "brushing": "...",
  "diet": "...",
  "prevention": "...",
  "nextStep": "..."
}`
}
